package main

import "fmt"

/*
Цифровой корень
Цифровой корень натурального числа — это цифра, полученная в результате итеративного процесса суммирования цифр,
на каждой итерации которого для подсчета суммы цифр берут результат,
полученный на предыдущей итерации. Этот процесс повторяется до тех пор, пока не будет получена одна цифра.

Например цифровой корень 65536 это 7 , потому что 6+5+5+3+6=25 и 2+5=7 .

По данному числу определите его цифровой корень.
Входные данные
Вводится одно натуральное число n, не превышающее 10^710
7
.
Выходные данные
Вывести цифровой корень числа n.
Sample Input:  3456
Sample Output: 9
*/
func main() {
	var a, b int
	fmt.Scan(&a)

	for a > 9 {
		b = a % 10
		a = a / 10
		a = a + b
	}
	fmt.Println(a)

	//fmt.Println((a-1)%9 + 1)
}
